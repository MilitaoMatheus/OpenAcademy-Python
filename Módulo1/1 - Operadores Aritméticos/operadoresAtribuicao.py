#Atribuição simples
saldo = 500
print(saldo)

#Atribuição com adição
saldo += 200
print(saldo)

#Atribuição com subtração
saldo -= 100
print(saldo)

#Atribuição com divisão
saldo /= 2
print(saldo)

#Atribuição com multiplicação
saldo *= 2
print(saldo)

#A atribuição se aplica a todos os operadores aritméticos